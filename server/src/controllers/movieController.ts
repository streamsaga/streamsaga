function extractStorageKey(url: string | undefined): string | null {
  if (!url) return null;
  if (url.includes('/uploads/')) {
    return url.split('/uploads/')[1];
  }
  try {
    const parsed = new URL(url);
    const pathname = parsed.pathname;
    const R2_BUCKET = process.env.R2_BUCKET;
    if (R2_BUCKET && pathname.startsWith(`/${R2_BUCKET}/`)) {
      return pathname.slice(R2_BUCKET.length + 2);
    }
    return pathname.startsWith('/') ? pathname.slice(1) : pathname;
  } catch (err) {
    return null;
  }
}

function extractJobId(hlsUrl: string | undefined): string | null {
  if (!hlsUrl) return null;
  const match = hlsUrl.match(/transcoded\/([^\/]+(?:\/[^\/]+)?)\/master\.m3u8/);
  return match ? match[1] : null;
}

export const createMovie = asyncHandler(async (req: AuthenticatedRequest, res: Response) => {
  const baseSlug = slugify(req.body.title);
  let slug = baseSlug;
  let suffix = 1;
  while (await Movie.exists({ slug })) {
    slug = `${baseSlug}-${suffix++}`;
  }

  const hlsUrl = req.body.hlsMasterPlaylistUrl;
  let initialStatus = 'draft';
  let qualities: any[] = [];

  const job = await TranscodeJob.findOne({ hlsMasterPlaylistUrl: hlsUrl });
  if (job) {
    if (job.status === 'completed') {
      initialStatus = 'draft';
      qualities = job.qualities;
    } else if (job.status === 'failed') {
      initialStatus = 'failed';
    } else {
      initialStatus = 'processing';
    }
  }

  const movieData = {
    ...req.body,
    slug,
    qualities,
    status: initialStatus,
    createdBy: req.user!.userId,
  };

  if (job && job.autoGeneratedPosterUrl && !req.body.poster) {
    movieData.poster = job.autoGeneratedPosterUrl;
  }

  const movie = await Movie.create(movieData);

  if (job) {
    job.movieId = movie._id as Types.ObjectId;
    await job.save();
  }

  res.status(201).json({ success: true, data: { movie } });
});

export const updateMovie = asyncHandler(async (req: AuthenticatedRequest, res: Response) => {
  const movie = await Movie.findById(req.params.id);
  if (!movie) throw new ApiError(404, 'Movie not found');

  if (req.body.title && req.body.title !== movie.title) {
    const baseSlug = slugify(req.body.title);
    let slug = baseSlug;
    let suffix = 1;
    while (await Movie.exists({ slug, _id: { $ne: movie._id } })) {
      slug = `${baseSlug}-${suffix++}`;
    }
    (req.body as any).slug = slug;
  }

  const hlsUrl = req.body.hlsMasterPlaylistUrl;
  if (hlsUrl && hlsUrl !== movie.hlsMasterPlaylistUrl) {
    const job = await TranscodeJob.findOne({ hlsMasterPlaylistUrl: hlsUrl });
    if (job) {
      job.movieId = movie._id as Types.ObjectId;
      await job.save();
      
      if (job.autoGeneratedPosterUrl && !req.body.poster) {
        req.body.poster = job.autoGeneratedPosterUrl;
      }
      
      if (job.status === 'completed') {
        req.body.status = 'draft';
        req.body.qualities = job.qualities;
      } else if (job.status === 'failed') {
        req.body.status = 'failed';
        req.body.qualities = [];
      } else {
        req.body.status = 'processing';
        req.body.qualities = [];
      }
    }
  }

  Object.assign(movie, req.body);
  await movie.save();

  res.json({ success: true, data: { movie } });
});

export const deleteMovie = asyncHandler(async (req: Request, res: Response) => {
  const movie = await Movie.findById(req.params.id);
  if (!movie) throw new ApiError(404, 'Movie not found');

  // Delete storage assets
  if (movie.poster) {
    const key = extractStorageKey(movie.poster);
    if (key) await deleteFile(key);
  }
  if (movie.banner) {
    const key = extractStorageKey(movie.banner);
    if (key) await deleteFile(key);
  }
  if (movie.trailerUrl) {
    const key = extractStorageKey(movie.trailerUrl);
    if (key) await deleteFile(key);
  }
  if (movie.hlsMasterPlaylistUrl) {
    const jobId = extractJobId(movie.hlsMasterPlaylistUrl);
    if (jobId) {
      await deleteDir(`transcoded/${jobId}`);
      await TranscodeJob.deleteOne({ hlsMasterPlaylistUrl: movie.hlsMasterPlaylistUrl });
    }
  }

  await Movie.findByIdAndDelete(req.params.id);
  res.json({ success: true, message: 'Movie deleted successfully' });
});

