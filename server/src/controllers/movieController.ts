export const getMovieBySlug = asyncHandler(async (req: Request, res: Response) => {
  const movie = await Movie.findOneAndUpdate(
    { slug: req.params.slug, status: 'published' },
    { $inc: { views: 1 } },
    { new: true }
  ).populate('genres categories');
  if (!movie) throw new ApiError(404, 'Movie not found');
  res.json({ success: true, data: { movie } });
});

export const listMovies = asyncHandler(async (req: Request, res: Response) => {
  const { page, limit, search, genre, category, status, featured, trending } = req.query as any;

  const filter: Record<string, unknown> = {};
  if (search) filter.$text = { $search: search as string };
  if (genre) filter.genres = genre;
  if (category) filter.categories = category;
  if (status) filter.status = status;
  if (featured !== undefined) filter.isFeatured = featured;
  if (trending !== undefined) filter.isTrending = trending;

  const skip = (page - 1) * limit;

  const [movies, total] = await Promise.all([
    Movie.find(filter).sort({ createdAt: -1 }).skip(skip).limit(limit).populate('genres categories'),
    Movie.countDocuments(filter),
  ]);

  res.json({
    success: true,
    data: {
      movies,
      pagination: {
        page,
        limit,
        total,
        totalPages: Math.ceil(total / limit),
      },
    },
  });
});

export const publishMovie = asyncHandler(async (req: Request, res: Response) => {
  const movie = await Movie.findById(req.params.id);
  if (!movie) throw new ApiError(404, 'Movie not found');
  if (movie.status === 'processing') {
    throw new ApiError(400, 'Cannot publish movie while video is still processing');
  }
  movie.status = 'published';
  await movie.save();
  res.json({ success: true, data: { movie } });
});

